# Sections and values names used in the configuration file (see appdownload.ini)
_APPS_LIST_SECTNAME = "applications"
_APP_INSTALL_KEYNAME = "install"
_APP_MODULE_KEYNAME = "module"
_APP_PACKAGE_NAME = "cots"
